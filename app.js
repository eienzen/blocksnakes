let web3;
let contract;
let account;

async function connectWallet() {
    if (window.ethereum) {
        try {
            web3 = new Web3(window.ethereum);
            const accounts = await window.ethereum.request({ method: 'eth_requestAccounts' });
            account = accounts[0];
            document.getElementById('account').innerText = account;
            contract = new web3.eth.Contract(contractABI, contractAddress);
            updateStatus("Wallet connected successfully!");
            getPlayerInfo(); // Auto-load player info after connecting
        } catch (error) {
            updateStatus("Error connecting wallet: " + error.message);
        }
    } else {
        updateStatus("Please install Metamask!");
    }
}

async function claimWelcomeBonus() {
    if (!contract) return updateStatus("Connect wallet first!");
    try {
        await contract.methods.claimWelcomeBonus().send({ from: account });
        updateStatus("Welcome bonus claimed successfully!");
        getPlayerInfo();
    } catch (error) {
        updateStatus("Error claiming bonus: " + error.message);
    }
}

async function claimRewards() {
    if (!contract) return updateStatus("Connect wallet first!");
    const rewardAmount = document.getElementById('rewardAmount').value;
    const referrer = document.getElementById('referrer').value || "0x0000000000000000000000000000000000000000";
    
    if (!rewardAmount) return updateStatus("Enter reward amount!");
    
    try {
        const amountInWei = web3.utils.toWei(rewardAmount, 'ether');
        await contract.methods.claimAllRewards(amountInWei, referrer).send({ from: account });
        updateStatus("Rewards claimed successfully!");
        getPlayerInfo();
    } catch (error) {
        updateStatus("Error claiming rewards: " + error.message);
    }
}

async function withdrawTokens() {
    if (!contract) return updateStatus("Connect wallet first!");
    try {
        const fee = await contract.methods.withdrawalFeeInBnb().call();
        await contract.methods.withdrawAllTokens().send({ 
            from: account, 
            value: fee 
        });
        updateStatus("Tokens withdrawn successfully!");
        getPlayerInfo();
    } catch (error) {
        updateStatus("Error withdrawing tokens: " + error.message);
    }
}

async function getPlayerInfo() {
    if (!contract) return updateStatus("Connect wallet first!");
    try {
        const balance = await contract.methods.getInternalBalance(account).call();
        const player = await contract.methods.playerHistory(account).call();
        
        document.getElementById('balance').innerText = web3.utils.fromWei(balance, 'ether') + " BST";
        document.getElementById('gamesPlayed').innerText = player.gamesPlayed;
        document.getElementById('totalRewards').innerText = web3.utils.fromWei(player.totalRewards, 'ether') + " BST";
        document.getElementById('referralRewards').innerText = web3.utils.fromWei(player.referralRewards, 'ether') + " BST";
    } catch (error) {
        updateStatus("Error fetching player info: " + error.message);
    }
}

async function getContractBalance() {
    if (!contract) return updateStatus("Connect wallet first!");
    try {
        const bstBalance = await contract.methods.contractBalance().call();
        const bnbBalance = await contract.methods.contractBnbBalance().call();
        
        document.getElementById('contractBalance').innerText = web3.utils.fromWei(bstBalance, 'ether') + " BST";
        document.getElementById('contractBnbBalance').innerText = web3.utils.fromWei(bnbBalance, 'ether') + " BNB";
    } catch (error) {
        updateStatus("Error fetching contract balance: " + error.message);
    }
}

function updateStatus(message) {
    document.getElementById('status').innerText = message;
}

// Initial check for Metamask
